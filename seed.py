def seed_default_accounts(db: Session) -> None:
    defaults = [
        ("admin", "admin@example.com", "123456", UserRole.ADMIN),
        ("farmer", "farmer@example.com", "123456", UserRole.FARMER),
        ("user", "user@example.com", "123456", UserRole.CONSUMER),
    ]
    for username, email, pwd, role in defaults:
        if db.query(User).filter(User.username == username).first():
            continue
        db.add(User(
            username=username,
            email=email,
            password_hash=hash_password(pwd),
            role=role,
            is_active=True,
            is_verified=True,
        ))
    db.commit()


